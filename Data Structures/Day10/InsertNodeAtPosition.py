def insertNodeAtPosition(head, data, position):
    tail = head
    node = SinglyLinkedListNode(data) #Creating Node
    n = position-1 
    while(n):
        tail = tail.next #Going one position before the actual inserting position
        n -= 1
    node.next = tail.next
    tail.next = node
    return head


class SinglyLinkedListNode:
    def __init__(self, node_data):
        self.data = node_data
        self.next = None

class SinglyLinkedList:
    def __init__(self):
        self.head = None
        self.tail = None

    def insert_node(self, node_data):
        node = SinglyLinkedListNode(node_data)

        if not self.head:   #If the Linked list is empty, point the head pointer to the first newly created node.
            self.head = node
        else:   #If the linked list has few elements then point the tail pointer of current node to new generated node
            self.tail.next = node


        self.tail = node #Point tail to the new added node i.e at the end.

def print_singly_linked_list(node, sep, fptr):
    while node:
        fptr.write(str(node.data))

        node = node.next

        if node:
            fptr.write(sep)

